# 统一 query 构造与检索执行
# ============================================================

def expand_knowledge_queries(query: str, limit: int = 4) -> list[str]:
    """把用户问题扩展为一小组领域感知检索词（ReAct 工具路径使用）。

    构造顺序：原文 → 理论术语精准 query（命中时）→ 领域规则 query。
    """
    text = (query or "").strip()
    queries = [text] if text else []
    match = detect_theory_topic(text)
    if match:
        queries.append(match[1])
    domain = detect_domain(text)
    if domain:
        queries.extend(DOMAIN_RULE_QUERIES.get(domain, ()))
    if not queries:
        queries.append("八字 命理 基础 大运 流年 用神")
    deduped: list[str] = []
    for q in queries:
        if q and q not in deduped:
            deduped.append(q)
    return deduped[:limit]


def search_deduped(
    queries: list[str],
    max_docs: int = 6,
    max_chars_per_query: int = 850,
    max_chars_total: int = 2500,
):
    """对多条 query 逐一检索并跨 query 去重，返回 [(query, doc), ...]。

    去重键：(来源文件, 内容前120字)，与 ReAct 工具原有行为一致。
    控量：单 query 检索结果累计 ≤ max_chars_per_query，总 ≤ max_chars_total。
    """
    docs = []
    seen = set()
    total_chars = 0
    for q in queries:
        query_chars = 0
        for doc in knowledge_base.search(q):
            key = (doc.metadata.get("source", ""), doc.page_content[:120])
            if key in seen:
                continue
            text_len = len(doc.page_content)
            if query_chars + text_len > max_chars_per_query:
                continue
            if total_chars + text_len > max_chars_total:
                return docs
            seen.add(key)
            docs.append((q, doc))
            query_chars += text_len
            total_chars += text_len
            if len(docs) >= max_docs:
                return docs
    return docs