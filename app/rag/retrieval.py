def detect_domain(text: str) -> str:
    """纯关键词打分识别领域，未命中返回空串。

    闲聊优先、年份兜底等意图层规则留在 workflow.classify_question，
    本函数只做检索策略所需的领域识别。
    """
    best_domain = ""
    best_score = 0
    for domain, keywords in DOMAIN_KEYWORDS.items():
        score = sum(1 for kw in keywords if kw in text)
        if score > best_score:
            best_domain = domain
            best_score = score
    return best_domain


